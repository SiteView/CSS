package com.siteview.css.topo.pojo;

public class Lamp {
	public int id;
	public String name;
	public boolean lampState;
	public Point p;
	public long Width;
	public long Heigth;

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public boolean isLampState() {
		return lampState;
	}
	public void setLampState(boolean lampState) {
		this.lampState = lampState;
	}
	public Point getP() {
		return p;
	}
	public void setP(Point p) {
		this.p = p;
	}
	public long getWidth() {
		return Width;
	}
	public void setWidth(long width) {
		Width = width;
	}
	public long getHeigth() {
		return Heigth;
	}
	public void setHeigth(long heigth) {
		Heigth = heigth;
	}
	
	
}
